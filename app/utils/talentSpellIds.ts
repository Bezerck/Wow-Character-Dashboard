// Helper: returns first spell ID for a talent key, or undefined if not populated.
export function getTalentSpellId(cls: string, key: string): number | undefined {
  const c = TALENT_SPELL_IDS[cls?.toLowerCase()];
  const arr = c?.[key];
  return arr && arr.length ? arr[0] : undefined;
}
